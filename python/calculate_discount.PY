def calculate_discount(price, discount_percent):
    """
    Calculate the final price after applying a discount.

    Parameters:
    - price (float): The original price of the item.
    - discount_percent (float): The discount percentage to apply.

    Returns:
    - float: The final price after applying the discount, or the original price if the discount is less than 20%.
    """
    if discount_percent >= 20:
        discount_amount = (discount_percent / 100) * price
        final_price = price - discount_amount
        return final_price
    else:
        return price
def main():
    try:
        # Prompt the user for the original price
        price = float(input("Enter the original price of the item: "))

        # Prompt the user for the discount percentage
        discount_percent = float(input("Enter the discount percentage: "))

        # Calculate the final price using the calculate_discount function
        final_price = calculate_discount(price, discount_percent)

        # Display the result
        if final_price < price:
            print(f"The final price after applying a {discount_percent}% discount is: ${final_price:.2f}")
        else:
            print(f"No discount applied. The original price remains: ${price:.2f}")

    except ValueError:
        print("Invalid input. Please enter numerical values for price and discount percentage.")

if __name__ == "__main__":
    main()
